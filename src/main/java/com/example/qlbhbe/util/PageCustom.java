package com.example.qlbhbe.util;

public class PageCustom {

  private String page;

  private String size;

  public PageCustom(String page, String size) {
    this.page = page;
    this.size = size;
  }


  public String getPage() {
    return page;
  }

  public void setPage(String page) {
    this.page = page;
  }

  public String getSize() {
    return size;
  }

  public void setSize(String size) {
    this.size = size;
  }
}
